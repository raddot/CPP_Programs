#include<iostream>
#include<cmath>
using namespace std;

int main()
{
    int num1 = 27;
    int num2 = -12;
    float num3 = 351.92;
    float num4 = 784.76;
    cout<<"sin(num1) :"<<sin(num1)<<endl;
    cout<<"abs(num2) :"<<abs(num2)<<endl;
    cout<<"floor(num3) :"<<floor(num3)<<endl;
    cout<<"sqrt(num4) :"<<sqrt(num4)<<endl;
    cout<<"pow(num1,2) :"<<pow(num1,2)<<endl;
    return 0;
}